package com.jsycloud.Player;

public class Time {
	
	public Time(int nYear, int nMonth, int nDay, int nHour, int nMinute, int nSecond){
		this.nYear = nYear;
		this.nMonth = nMonth;
		this.nDay = nDay;
		this.nHour = nHour;
		this.nMinute = nMinute;
		this.nSecond = nSecond;
	}

	private int nYear;
	private int nMonth;
	private int nDay;
	private int nHour;
	private int nMinute;
	private int nSecond;
}
